class Article {
  final String id;
  final String title;
  final String description;
  final double price;
  final String imageUrl;
  final String categoryId;
  final String categoryTitle;

  Article({
    required this.id,
    required this.title,
    required this.description,
    required this.price,
    required this.imageUrl,
    required this.categoryId,
    required this.categoryTitle,
  });
}
